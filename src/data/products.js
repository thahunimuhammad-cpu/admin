// Helper functions
export const getProductById = (id) => {
  return products.find((p) => p.id === parseInt(id));
};

export const getProductsByCategory = (category) => {
  return products.filter((p) => p.category === category);
};

export const getProductsByCollection = (collectionSlug) => {
  return products.filter((p) => p.collections.includes(collectionSlug));
};

export const getProductsByVendor = (vendor) => {
  return products.filter((p) => p.vendor === vendor);
};

export const getNewProducts = () => getProductsByCollection("new-products");
export const getBestSellers = () => getProductsByCollection("best-sellers").sort((a, b) => b.reviews - a.reviews);
export const getSpecialOffers = () => {
  return products.filter((p) => p.originalPrice > p.price).sort((a, b) => {
    const discountA = ((a.originalPrice - a.price) / a.originalPrice) * 100;
    const discountB = ((b.originalPrice - b.price) / b.originalPrice) * 100;
    return discountB - discountA;
  });
};
